/*
 * Exynos9830 Manual
 *  3.  Mamory Map
 *  3.2 DPP Base address
 *  <Table 3-2 Base Address of Each IP> IP: DPP, Base Address: 0x1904_0000
 *
 * DPP MCD CM SFR base address : 0x19040000 
 * - DPP L0   : 0x19048000
 * - DPP L1   : 0x19048800
 * - DPP L2   : 0x19049000
 * - DPP L3   : 0x19049800
 * - DPP L4   : 0x1904A000
 * - DPP L5   : 0x1904A800
 */
#define DPP_MCD_CM_BASE_ADDR    0x19040000

#define DPP_MCD_CM_L0_ADDR     0x8000
#define DPP_MCD_CM_L1_ADDR     0x8800
#define DPP_MCD_CM_L2_ADDR     0x9000
#define DPP_MCD_CM_L3_ADDR     0x9800
#define DPP_MCD_CM_L4_ADDR     0xA000
#define DPP_MCD_CM_L5_ADDR     0xA800

#define DPP_MCD_CM_CON_SZ           1   /* Byte size : 0x004 */
#define DPP_MCD_CM_GM_SZ            5   /* Byte size : 0x014 */
#define DPP_MCD_CM_EOTF_SZ          96  /* Byte size : 0x180 */
#define DPP_MCD_CM_OETF_SZ          33  /* Byte size : 0x084 */
#define DPP_MCD_CM_SC_SZ            1   /* Byte size : 0x004 */

#define DPP_MCD_CM_TM_DYNAMIC_SZ    40  /* Byte size : 0x0A0 */
#define DPP_MCD_CM_TM_TUNE_SZ       1   /* Byte size : 0x004 */
#define DPP_MCD_CM_TM_GAMUT_SZ      2   /* Byte size : 0x008 */
#define DPP_MCD_CM_TM_CURVE_SZ      30  /* Byte size : 0x078 */
#define DPP_MCD_CM_TM_EXT_SZ        7   /* Byte size : 0x01C */
#define DPP_MCD_CM_TM_OFF_SZ        1   /* Byte size : 0x004 */


#define DPP_MCD_CM_CON_ADDR             ((0x000)             )
#define DPP_MCD_CM_GM_ADDR(_n)          ((0x004) + (_n * 0x4))
#define DPP_MCD_CM_EOTF_ADDR(_n)        ((0x018) + (_n * 0x4))
#define DPP_MCD_CM_OETF_ADDR(_n)        ((0x198) + (_n * 0x4))
#define DPP_MCD_CM_SC_ADDR(_n)          ((0x21C) + (_n * 0x4))
#define DPP_MCD_CM_TM_TUNE_ADDR(_n)     ((0x220) + (_n * 0x4))
#define DPP_MCD_CM_TM_GAMUT_ADDR(_n)    ((0x224) + (_n * 0x4))
#define DPP_MCD_CM_TM_CURVE_ADDR(_n)    ((0x22C) + (_n * 0x4))
#define DPP_MCD_CM_TM_EXT_ADDR(_n)      ((0x2A4) + (_n * 0x4))
#define DPP_MCD_CM_TM_DYNAMIC_ADDR(_n)  ((0x220) + (_n * 0x4))
#define DPP_MCD_CM_TM_OFF_ADDR(_n)      ((0x234) + (_n * 0x4))

